function chartLine(chartid, legend, values, tooltips, series, labels) {
	$("#" + chartid).chart( {
		legend : legend,
		series : series,
		values : values,
		tooltips : tooltips,		
		labels : labels,
		template : "tmpl_line",
		margins : [ 30, 40, 5, 40 ],
		barMargins : 10
	});
}

function chartBar(chartid, legend, values, tooltips, series, labels) {
	$("#" + chartid).chart( {
		legend : legend,
		values : values,
		tooltips : tooltips,
		series : series,
		labels : labels,
		template : "tmpl_bar"
	});
}

function chartArea(chartid, legend, values, tooltips, series, labels) {
	$("#" + chartid).chart( {
		legend : legend,
		values : values,
		tooltips : tooltips,
		series : series,
		labels : labels,
		template : "tmpl_line",
		margins : [ 30, 40, 20, 40 ],
		defaultSeries : {
			fill : true 
		}
	});
}

